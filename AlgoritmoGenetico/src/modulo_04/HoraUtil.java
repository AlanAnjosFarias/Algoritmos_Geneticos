package modulo_04;

public class HoraUtil {
	private final int horaUtilID;
	private final String horaUtil;
	
	public HoraUtil(int horaUtilID, String horaUtil) {
		this.horaUtilID = horaUtilID;
		this.horaUtil = horaUtil;
	}
	
	public int getHoraUtilID() {
		return this.horaUtilID;
	}
	
	public String getHoraUtil() {
		return this.horaUtil;
	}

}
